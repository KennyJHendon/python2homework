the_list = [1,14,11,5,8,9]


def lessThanTen(a_list):
    new_list = []
    for num in a_list:
        if num <= 10:
            new_list.append(num)
        else:
            continue
        
    
            
        return(new_list)
            
   
        

lessThanTen(the_list)